package setExamples;

import java.util.HashSet;


public class HashSetEx {

	public static void main(String[] args) {
		HashSet<String> set = new HashSet<String>();
		set.add(null);
		set.add("b");
		set.add("c");
		set.add("a");
		set.add("d");
		set.add("z");
		set.add("v");
		set.add("r");
		set.add("t");
		set.add("y");
		set.add("s");
		set.add("p");
		set.add("o");
		System.out.println(set);
	
	}

}
